//Igual (==)
//Retorna verdadeiro caso os operandos sejam iguais 
"3" == var1
3 == '3'

//Não igual (!=)
//Retorna verdadeiro caso os operadores não sejam iguais
var2 -= '3'

//Estritamente igual (===)
//Retorna verdadeiro caso os operadores sejam iguais e do mesmo tipo
3 === var1

//Estritamente não iguais (!==)
//Retorna verdadeiro caso os operadores não sejam iguais e/ou não sejam do mesmo tipo
var1 !== "3"
3 !== '3'

//Maior que (>)
//Retorna verdeiro caso o operador da esqueda seja maior que o da direita
var2 > var1
"12" > 2

//Menor que (<)
//Retorna verdeiro caso o operador da esqueda seja menor que o da direita
var1 < var2
"12" < "2"

//Menor que ou igual (<=)
//Retorna verdeiro caso o operador da esquerda seja menor ou igual ao da direita
var1 <= var2
var2 <= 5

